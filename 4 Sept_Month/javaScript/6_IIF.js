
// Immediately Invoked Function Expressions (IIFE)
// Uses of IIFE
// Run code immediately (no need to call separately).
// Keep variables private (avoid global pollution).
// Useful for initialization/setup.
// Used in module pattern (before ES6).

(function () {
    console.log("Immediately invoked function expressions");
})();


// Self-Invoking Function
(function () {
    console.log("Self invoking fun.");
})();
